function calculadoraMostrar() {
    var div = document.getElementById('calcu-container');
    if (div.style.display === 'none') {
        div.style.display = 'block';
    } else {
        div.style.display = 'none';
    }
}

function temporizadorMostrar() {
    var div = document.getElementById('tempo-container');
    if (div.style.display === 'none') {
        div.style.display = 'block';
    } else {
        div.style.display = 'none';
    }
}